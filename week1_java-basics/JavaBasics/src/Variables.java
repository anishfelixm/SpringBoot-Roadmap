public class Variables {
    public static void main(String[] args) {
        String name = "World";
        System.out.println("Hello, " + name);

        float PI = 3.141592f;
        System.out.println("PI: " + PI);

        double PI2 = 3.141592653;
        System.out.println("PI (more decimals): " + PI2);
    }
}